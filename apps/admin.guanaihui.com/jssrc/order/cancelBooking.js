function CancelBookingController() {
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
     继承于Controller基类
     -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    Controller.call(this);

    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
     绑定事件
     -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.bind();
}



/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
绑定事件
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
CancelBookingController.prototype.bind = function() {
    var classSelf = this;

    $(".op .save").on("click", function() {
        classSelf.confirm({
            title: "取消订单确认",
            content: "<div style='margin-left:10px;font-size:14px;'>请确认：同意为用户取消订单</span><br/><span>点击确定将订单状态流转至</span> <span class='text-danger'>「已取消」</span><span>状态</span></div>",
            confirmInterface: function() {
                $("#detailsForm").submit();
            }
        });
    });
}


/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
类的初始化
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
$(document).ready(function() {
    new CancelBookingController;
});
