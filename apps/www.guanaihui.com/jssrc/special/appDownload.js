function AppDownLoadController() {
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
     继承于Controller基类
     -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    Controller.call(this);

    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    绑定事件
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.bindEvents();
};



/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
 绑定事件
 -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
AppDownLoadController.prototype.bindEvents = function() {
    $("#btnIos").on("click", function() {
        ga('send', 'event', 'IOS', 'download', 'IOS下载');
    });

    $("#btnAndroid").on("click", function() {
        ga('send', 'event', 'ANDROID', 'download', 'ANDROID下载');
    });
}

/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
 类的初始化
 -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
$(document).ready(function() {
    new AppDownLoadController;
});
